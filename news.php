			<div class="blog-article py-12 ">
				<p class="pt-4"><strong>Kokostezky</strong> jsou přírodní traily převážně v okolí Kokořínského údolí, ale i v dalších částech Kokořínska.</p>
				<p class="pt-4">Přestože se na Kokořínsku jezdilo na horských kolech už dávno před vznikem spolku, parta nadšenců a místních obyvatel se rozhodla založit Kokostezky, objevovat nové traily, čistit staré a nepoužívané lesní cesty a myslivecké chodníky, aby se trailové ježdění přesunulo co nejvíce mimo turisticky značené cesty a bylo zábavnější.</p>
				<p class="pt-4">Rostoucí popularita a počet bikerů přijíždějících na Kokostezky, má nezanedbatelný dopad na celkovou atmosféru lokality, turistický ruch, ale i přímo na traily. Aby byl tento dopad co nejvíce pozitivní, dovolujeme si předat několik doporučení a pravidel, aby nám trailové ježdění na Kokořínsku vydrželo, bylo stále tak pěkné a mohli jsme Kokostezky udržovat v dobré kondici a postupně přijíždějícím cyklistům nabízet další zajímavé traily a celkově kvalitnější servis.</p>
